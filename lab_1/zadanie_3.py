liczba = 1.0
print("Stan początkowy: ", liczba)
liczba += 3
print("Po dodaniu: 3 = ",  liczba)
liczba -= 2
print("Po odjęciu: 2 = ", liczba)
liczba *= 3
print("Po przemnożeniu przez: 3 = ", liczba)
liczba /= 2
print("Po podzieleniu przez: 2 = ", liczba)
liczba **= 2
print("Po podniesieniu do potęgi: 2 = ", liczba)
liczba %= 6
print("Po wykonaniu modulo przez: 6 = ", liczba)

